# Зчитуємо два числа з консолі
a = int(input("Введіть перше ціле число: "))
b = int(input("Введіть друге ціле число: "))

# Обчислюємо суму, різницю і добуток
сума = a + b
різниця = a - b
добуток = a * b

# Виводимо результати обчислень
print(f"Сума: {сума}")
print(f"Різниця: {різниця}")
print(f"Добуток: {добуток}")